injectionVertexSolutions[V_?StringQ,inpData_,rho_,t_,dimensional_:True,prime_:True]:= Module[{data,out},

data = inputDataTransformation[inpData,prime];

Switch[V,
		"M", If[dimensional,
		out = mVertexSolutionDimensional[rho,n/.data,V,inpData,t,prime];,
		out = mVertexSolution[rho,n/.data];];,
		"K",
		If[dimensional,
		out = kVertexSolutionDimensional[rho,V,inpData,t,prime];,
		out = kVertexSolution[rho];];,
		"Mt",
		If[dimensional,
		out = mTildeVertexSolutionDimensional[rho,n/.data,V,inpData,t,prime];,
		out = mTildeVertexSolution[rho,n/.data];];,
		"Kt",
		If[dimensional,
		out = kTildeVertexSolutionDimensional[rho,V,inpData,t,prime];,
		out = kTildeVertexSolution[rho];];	
];
out
];


(* Auxiliary functions for M and M~ vertex*)
G[i_, p_, q_, rho_] :=
    (i!*Gamma[p + i])/Gamma[p + 2*i]*JacobiP[i, p - q, q - 1, 2*rho - 1]
h[i_, p_, q_] :=
    (i!*Gamma[i + q]*Gamma[i + p]*Gamma[i + p - q + 1])/((2 i + p)*Gamma[2 i + p]^2)

